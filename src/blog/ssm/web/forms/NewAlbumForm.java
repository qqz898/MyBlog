package blog.ssm.web.forms;

public class NewAlbumForm {

	private String album_name;
	private String album_intro;
	private String ifsecured;
	public String getAlbum_name() {
		return album_name;
	}
	public void setAlbum_name(String album_name) {
		this.album_name = album_name;
	}
	public String getAlbum_intro() {
		return album_intro;
	}
	public void setAlbum_intro(String album_intro) {
		this.album_intro = album_intro;
	}
	public String getIfsecured() {
		return ifsecured;
	}
	public void setIfsecured(String ifsecured) {
		this.ifsecured = ifsecured;
	}
	
	
}

package blog.ssm.domain;

public class ArtCat {

	private int art_cat_id;
	private String cat_name;
	private int cat_user;
	
	public int getArt_cat_id() {
		return art_cat_id;
	}
	public void setArt_cat_id(int art_cat_id) {
		this.art_cat_id = art_cat_id;
	}
	public String getCat_name() {
		return cat_name;
	}
	public void setCat_name(String cat_name) {
		this.cat_name = cat_name;
	}
	public int getCat_user() {
		return cat_user;
	}
	public void setCat_user(int cat_user) {
		this.cat_user = cat_user;
	}
	
}
